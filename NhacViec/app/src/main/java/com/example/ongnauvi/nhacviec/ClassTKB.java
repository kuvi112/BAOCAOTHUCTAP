public class ClassTKB {
    private int Thu;
    private String T12;
    private String T34;
    private String T67;
    private String T89;
    private String T1112;
    private String T1314;

    public ClassTKB()
    { }

    public ClassTKB(int thu, String t12, String t34, String t67, String t89, String t1112, String t1314) {
        Thu = thu;
        T12 = t12;
        T34 = t34;
        T67 = t67;
        T89 = t89;
        T1112 = t1112;
        T1314 = t1314;
    }

    public int getThu() {
        return Thu;
    }

    public void setThu(int thu) {
        Thu = thu;
    }

    public String getT12() {
        return T12;
    }

    public void setT12(String t12) {
        T12 = t12;
    }

    public String getT34() {
        return T34;
    }

    public void setT34(String t34) {
        T34 = t34;
    }

    public String getT67() {
        return T67;
    }

    public void setT67(String t67) {
        T67 = t67;
    }

    public String getT89() {
        return T89;
    }

    public void setT89(String t89) {
        T89 = t89;
    }

    public String getT1112() {
        return T1112;
    }

    public void setT1112(String t1112) {
        T1112 = t1112;
    }

    public String getT1314() {
        return T1314;
    }

    public void setT1314(String t1314) {
        T1314 = t1314;
    }
}
